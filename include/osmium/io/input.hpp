#include <functional>
#include <map>
#include <memory>
#include <string>
#include <vector>

#include <osmium/io/file.hpp>
#include <osmium/io/meta.hpp>
#include <osmium/memory/buffer.hpp>

namespace osmium {

    namespace io {

        /**
         * Virtual base class for all input classes.
         */
        class Input {

            osmium::OSMFile m_file;

            osmium::io::Meta m_meta;

            Input(const Input&) = delete;
            Input(Input&&) = delete;

            Input& operator=(const Input&) = delete;
            Input& operator=(Input&&) = delete;

        public:

            virtual ~Input() {
            }

            virtual osmium::io::Meta read(bool header_only) = 0;

            virtual osmium::memory::Buffer next_buffer() = 0;

        protected:

            Input(const osmium::OSMFile& file) :
                m_file(file) {
                m_meta.has_multiple_object_versions(m_file.has_multiple_object_versions());
                m_file.open_for_input();

            }

            osmium::io::Meta& meta() {
                return m_meta;
            }

            const osmium::OSMFile& file() const {
                return m_file;
            }

            int fd() const {
                return m_file.fd();
            }

        }; // class Input

        class InputFactory {

        public:

            typedef std::function<osmium::io::Input*(const osmium::OSMFile&)> create_input_type;

        private:

            typedef std::map<osmium::io::Encoding*, create_input_type> encoding2create_type;

            encoding2create_type m_callbacks;

            InputFactory() :
                m_callbacks() {
            }

        public:

            static InputFactory& instance() {
                static InputFactory factory;
                return factory;
            }

            bool register_input_format(std::vector<osmium::io::Encoding*> encodings, create_input_type create_function) {
                for (auto encoding : encodings) {
                    if (! m_callbacks.insert(encoding2create_type::value_type(encoding, create_function)).second) {
                        return false;
                    }
                }
                return true;
            }

            bool unregister_input_format(osmium::io::Encoding* encoding) {
                return m_callbacks.erase(encoding) == 1;
            }

            std::unique_ptr<osmium::io::Input> create_input(const osmium::OSMFile& file) {
                encoding2create_type::iterator it = m_callbacks.find(file.encoding());

                if (it != m_callbacks.end()) {
                    return std::unique_ptr<osmium::io::Input>((it->second)(file));
                }

                throw osmium::OSMFile::FileEncodingNotSupported();
            }

        }; // class InputFactory

        class Reader {

            osmium::OSMFile m_file;
            std::unique_ptr<Input> m_input;

            Reader(const Reader&) = delete;
            Reader& operator=(const Reader&) = delete;

            template <class THandler>
            osmium::item_type push_helper(osmium::memory::Buffer& buffer, osmium::item_type type, THandler& handler) {
                return handler(buffer, type);
            }

            template <class THead, class ...TTail>
            osmium::item_type push_helper(osmium::memory::Buffer& buffer, osmium::item_type type, THead& handler, TTail&... more) {
                handler(buffer, type);
                return push_helper(buffer, type, more...);
            }

            template <class THandler>
            void push_helper(osmium::item_type type, THandler& handler) {
                handler(type);
            }

            template <class THead, class ...TTail>
            void push_helper(osmium::item_type type, THead& handler, TTail&... more) {
                handler(type);
                push_helper(type, more...);
            }

        public:

            Reader(const osmium::OSMFile& file) :
                m_file(file),
                m_input(InputFactory::instance().create_input(m_file)) {
            }

            Reader(const std::string& filename = "") :
                m_file(filename),
                m_input(InputFactory::instance().create_input(m_file)) {
            }

            osmium::io::Meta open(bool header_only=false) {
                return m_input->read(header_only);
            }

            osmium::memory::Buffer read() {
                return m_input->next_buffer();
            }

            template <class ...THandlers>
            void push(THandlers&... handlers) {
                osmium::item_type type = osmium::item_type::undefined;
                while (osmium::memory::Buffer buffer = read()) {
                    type = push_helper(buffer, type, handlers...);
                }
                push_helper(type, handlers...);
            }

        }; // class Reader

    } // namespace io

} // namespace osmium

