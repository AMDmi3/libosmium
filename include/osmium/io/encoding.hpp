#include <string>

namespace osmium {

    /**
     * @brief Namespace for everything related to input and output of OSM data.
     */
    namespace io {

        /**
         * Instances of this class describe different file encodings (ie PBF,
         * XML or different compressed versions of XML).
         *
         * You can not create instances of this class yourself, instead use
         * the static methods provided to get the predefined instances.
         */
        class Encoding {

            const std::string m_suffix;
            const std::string m_description;
            const std::string m_compress;

            Encoding(const std::string& suffix, const std::string& description, const std::string& compress = "") :
                m_suffix(suffix),
                m_description(description),
                m_compress(compress) {
            }

            Encoding(const Encoding&) = delete;
            Encoding& operator=(const Encoding&) = delete;

            Encoding(Encoding&&) = delete;
            Encoding& operator=(Encoding&&) = delete;

            ~Encoding() = default;

        public:

            const std::string& suffix() const {
                return m_suffix;
            }

            const std::string& description() const {
                return m_description;
            }

            const std::string& compress() const {
                return m_compress;
            }

            /**
             * PBF encoding.
             */
            static Encoding* PBF() {
                static Encoding instance(".pbf", "PBF");
                return &instance;
            }

            /**
             * XML encoding, uncompressed.
             */
            static Encoding* XML() {
                static Encoding instance("", "XML (uncompressed)");
                return &instance;
            }

            /**
             * XML encoding, compressed with gzip.
             */
            static Encoding* XMLgz() {
                static Encoding instance(".gz", "XML (compressed with gzip)", "gzip");
                return &instance;
            }

            /**
             * XML encoding, compressed with bzip2.
             */
            static Encoding* XMLbz2() {
                static Encoding instance(".bz2", "XML (compressed with bzip2)", "bzip2");
                return &instance;
            }

            /**
             * OPL encoding, uncompressed.
             */
            static Encoding* OPL() {
                static Encoding instance(".opl", "OPL (uncompressed)");
                return &instance;
            }

            /**
             * OPL encoding, compressed with gzip.
             */
            static Encoding* OPLgz() {
                static Encoding instance(".opl.gz", "OPL (compressed with gzip)", "gzip");
                return &instance;
            }

            /**
             * OPL encoding, compressed with bzip2.
             */
            static Encoding* OPLbz2() {
                static Encoding instance(".opl.bz2", "OPL (compressed with bzip2)", "bzip2");
                return &instance;
            }

        }; // class Encoding

    } // namespace io

} // namespace osmium

#endif // OSMIUM_IO_ENCODING_HPP
