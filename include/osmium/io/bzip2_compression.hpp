#define OSMIUM_LINK_WITH_LIBS_ZLIB -lbz2

#include <cstdio>

#include <bzlib.h>

#include <osmium/io/compression.hpp>

namespace osmium {

    namespace io {

        class Bzip2Compression : public Compression {

            FILE* m_file;
            int m_bzerror;
            BZFILE* m_bzfile;

        public:

            Bzip2Compression(int fd, bool write) :
                Compression(),
                m_file(fdopen(fd, "w")),
                m_bzerror(BZ_OK),
                m_bzfile(::BZ2_bzWriteOpen(&m_bzerror, m_file, 6, 0, 0)) { // XXX write vs read
                if (!m_bzfile) {
                    throw std::runtime_error("initialization of bzip2 compression failed");
                }
            }

            ~Bzip2Compression() override final {
                this->close();
            }

            std::string read() override final {
                // XXX
            }

            void write(const std::string& data) override final {
                int error;
                ::BZ2_bzWrite(&error, m_bzfile, const_cast<char*>(data.data()), data.size());
                // XXX check for error
            }

            void close() override final {
                if (m_bzfile) {
                    int error;
                    unsigned int nbytes_in_lo32;
                    unsigned int nbytes_in_hi32;
                    unsigned int nbytes_out_lo32;
                    unsigned int nbytes_out_hi32;
                    ::BZ2_bzWriteClose64(&error, m_bzfile, 0, &nbytes_in_lo32, &nbytes_in_hi32, &nbytes_out_lo32, &nbytes_out_hi32);
                    m_bzfile = nullptr;
                }
            }

        }; // class Bzip2Compression

        namespace {

            const bool registered_bzip2_compression = osmium::io::CompressionFactory::instance().register_compression({
                "bzip2"
            }, [](int fd, bool write) {
                return new osmium::io::Bzip2Compression(fd, write);
            });

        } // anonymous namespace

    } // namespace io

} // namespace osmium

#endif // OSMIUM_IO_BZIP2_COMPRESSION_HPP
