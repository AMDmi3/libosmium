#include <vector>

#include <osmium/area/problem.hpp>
#include <osmium/area/problem_reporter.hpp>

namespace osmium {

    namespace area {

        class ProblemReporterCollector : public ProblemReporter {

        protected:

            // Problems found
            std::vector<osmium::area::ProblemPoint> m_problem_points {};
            std::vector<osmium::area::ProblemLine>  m_problem_lines  {};

        public:

            ProblemReporterCollector() = default;

            virtual ~ProblemReporterCollector() = default;

            void report_duplicate_node(osmium::object_id_type node_id1, osmium::object_id_type node_id2, osmium::Location location) override {
                m_problem_points.emplace_back(osmium::area::ProblemPoint(osmium::area::Problem::problem_type::duplicate_node, node_id1, node_id2, location));
            }

            void report_intersection(osmium::object_id_type object_id, osmium::object_id_type way1_id, osmium::Location way1_seg_start, osmium::Location way1_seg_end,
                                                                       osmium::object_id_type way2_id, osmium::Location way2_seg_start, osmium::Location way2_seg_end, osmium::Location intersection) override {
                m_problem_points.emplace_back(osmium::area::ProblemPoint(osmium::area::Problem::problem_type::intersection, object_id, 0, intersection));
                m_problem_lines.emplace_back(osmium::area::ProblemLine(osmium::area::Problem::problem_type::intersection, object_id, way1_id, way1_seg_start, way1_seg_end));
                m_problem_lines.emplace_back(osmium::area::ProblemLine(osmium::area::Problem::problem_type::intersection, object_id, way2_id, way2_seg_start, way2_seg_end));
            }

            void report_ring_not_closed(osmium::object_id_type object_id, osmium::Location end1, osmium::Location end2) override {
                m_problem_points.emplace_back(osmium::area::ProblemPoint(osmium::area::Problem::problem_type::ring_not_closed, object_id, 0, end1));
                m_problem_points.emplace_back(osmium::area::ProblemPoint(osmium::area::Problem::problem_type::ring_not_closed, object_id, 0, end2));
            }
            
            void report_role_should_be_outer(osmium::object_id_type object_id, osmium::object_id_type way_id, osmium::Location seg_start, osmium::Location seg_end) override {
                m_problem_lines.emplace_back(osmium::area::ProblemLine(osmium::area::Problem::problem_type::role_should_be_outer, object_id, way_id, seg_start, seg_end));
            }

            void report_role_should_be_inner(osmium::object_id_type object_id, osmium::object_id_type way_id, osmium::Location seg_start, osmium::Location seg_end) override {
                m_problem_lines.emplace_back(osmium::area::ProblemLine(osmium::area::Problem::problem_type::role_should_be_inner, object_id, way_id, seg_start, seg_end));
            }

        }; // class ProblemReporterCollector

    } // namespace area

} // namespace osmium

#endif // OSMIUM_AREA_PROBLEM_REPORTER_COLLECTOR_HPP
