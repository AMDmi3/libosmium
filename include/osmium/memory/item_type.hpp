#include <cstdint>

namespace osmium {

    enum class item_type : uint32_t {

        undefined                              = 0x00,
        node                                   = 0x01,
        way                                    = 0x02,
        relation                               = 0x03,
        collection                             = 0x10,
        tag_list                               = 0x11,
        way_node_list                          = 0x12,
        way_node_with_location_list            = 0x32,
        relation_member_list                   = 0x13,
        relation_member_list_with_full_members = 0x33

    }; // enum class item_type

    inline item_type char_to_item_type(const char c) {
        switch (c) {
            case 'n':
                return item_type::node;
            case 'w':
                return item_type::way;
            case 'r':
                return item_type::relation;
            default:
                return item_type::undefined;
        }
    }

    inline char item_type_to_char(const item_type type) {
        switch (type) {
            case item_type::node:
                return 'n';
            case item_type::way:
                return 'w';
            case item_type::relation:
                return 'r';
            default:
                return '-';
        }
    }

    inline const char* item_type_to_name(const item_type type) {
        switch (type) {
            case item_type::node:
                return "node";
            case item_type::way:
                return "way";
            case item_type::relation:
                return "relation";
            default:
                return "-";
        }
    }

    inline item_type item_type_for_collection_of(item_type type) {
        return static_cast<item_type>(static_cast<uint32_t>(type) | static_cast<uint32_t>(item_type::collection));
    }

} // namespace osmium

#endif // OSMIUM_MEMORY_ITEM_TYPE_HPP
